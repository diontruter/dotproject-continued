	<td width=50%>
		<p><b>Version 0.2.3</b>
		
		<p><b>dotproject</b> is a PHP+MySQL beta level web based project management tool that dotmarketing started in Dec. 2000. It is about 70% along to where we think it should be.

		<p>While dotproject was specifically designed for dotmarketing's needs, it could probably be extremely useful for any sort of service agency that requires the ability to track a project to completion.

		<p>The current version of dotproject is .2, which reflects its immaturity fairly accurately. Still, we feel that there is a lot there that is much nicer than other "project management" systems, which seem to have the feel of "programmer only" projects.

		<p><b>What New in this Version</b>

		<ul type=square>
			<li>Ability to watch forums or forum topics
			<li>Hierarchial Company Departments module
			<li>Improved filtering of index screens
			<li>Standardising look-and-feel across modules as well as view enhancements (tabbed and flat views)
			<li>Support for user types
			<li>Individualise events with the ability to mark as private
		</ul>

